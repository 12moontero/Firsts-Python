#OPERADORES ASIMETRICOS

print(5+5) #suma

print(5-3) #RESTA

print(5*2) #MULTIPLIICAR

print(5/2) #DIVISION (CON DECIMALES)


print(5//2) #DVISION ENTERA (SIN DECIMALES)

print(5%2) #OPERADOR DE MODULO O RESIDUO (ES EL NUEMRO QEU SOBRA EN UNA DIVISION SIN DECIMALES ( SE PONE % ))

print(2**3) #ESTE OPERADOR ES DE LA POTENCIA (SE PONE DOS MULTIPLICADORES **)

numero_uno = 50
numero_dos = 20

print(numero_dos**numero_uno)

print(numero_uno+numero_dos)            

print(numero_dos%numero_uno)        

print(numero_uno-numero_dos)

suma=numero_uno+numero_dos
resta=numero_dos-numero_uno        #PODEMOS METER EN UNA VARIABLE UNA OPERACIO   [ASI PODEMOS AHCER OPERACCIONES GIGANTESCAS]

print(suma+resta)           #OPERACION DE DOS VARIABLES Y ESAS MISMAS VARIABLES OPERACIONES

print(f"la suma de {numero_uno} y {numero_dos} es {suma}")

