#OPERADORES LOGICOS

#OPERADOR ------------------ and
#OPERADOR ------------------ or
#OPERADOR ------------------ not          Invierte el valor boolenano

print(True and False)     #False
print(True or False)      #True
print(not True)           #False
print(not False)          #True


x = 10
y = 5

print(x > 5 and y < 10)      #True
print(not(x < 5))            #True


